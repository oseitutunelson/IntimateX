// Define Schema
const videoSchema = new mongoose.Schema({
  videoId: { type: String, required: true, unique: true },
  views: { type: Number, default: 0 },
});

const Video = mongoose.model('Video', videoSchema);

//increment views and reward
const relayTransaction = async (userAddress, imgHash) => {
  const contractAddress = "0x4Ea4cdC718b8A4ef44a932d5b1AbD49f7CEb72BF";
  const provider = new ethers.providers.Web3Provider(window.ethereum);
  const signer = new ethers.Wallet("0x681f8d7f47808db4623ecd36e8a14f947c1aa278cd217e61b3faff50c50e2215", provider);

  const contract = new ethers.Contract(contractAddress, abi, signer);

  try {
    const tx = await contract.incrementViews(userAddress, imgHash);
    console.log("Transaction Hash:", tx.hash);
    await tx.wait();
    console.log("Transaction confirmed");
  } catch (error) {
    console.error("Error relaying transaction:", error);
  }
};
// Routes

// Increment View Count
app.post('/api/videos/view', async (req, res) => {
  const { creator , videoId } = req.body;

  try {
    const video = await Video.findOneAndUpdate(
      { videoId },
      { $inc: { views: 1 } },
      { new: true, upsert: true } // Create a new entry if it doesn't exist
    );
    res.status(200).json(video);
    await relayTransaction(creator,videoId);
  } catch (err) {
    res.status(500).json({ error: 'Failed to update view count' });
  }
});

// Get View Count
app.get('/api/videos/:videoId', async (req, res) => {
  const { videoId } = req.params;

  try {
    const video = await Video.findOne({ videoId });
    if (!video) return res.status(404).json({ error: 'Video not found' });
    res.status(200).json(video);
  } catch (err) {
    res.status(500).json({ error: 'Failed to fetch view count' });
  }
});

